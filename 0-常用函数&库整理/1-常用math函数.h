#include <cstdio>
#include <cmath>

void f_fabs(){
    double db = -12.56;
    double fabs_db = fabs(db);    /*绝对值*/
    double floor_db = floor(db);    /*向下取整*/
    double ceil_db = ceil(db);  /*向上取整*/
    std::printf("%.0f=fabs %.0f=floor %.0f=ceil", fabs_db, floor_db, ceil_db);


}



